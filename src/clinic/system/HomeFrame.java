    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
       
        // search patient from db and rerurn data into textFields 
        try{
            
         Connection con = DBConnection.get_DefaultConnection();
         Statement st;
         st = con.createStatement(); 
         
         // select all patients into table
         String select_sql = "SELECT `name`, `age`, `address`, `phone_number`, "
                + "`temp`, `patient_job`, `money_paid`, `money_remaining` FROM `patient_table` WHERE name = '"+search_tf1.getText()+"'";
         
          ResultSet rs = st.executeQuery(select_sql);
         
               
          if(rs.next()){
        
           String name = rs.getString("name");
           String age = rs.getInt("age")+"";
           String address = rs.getString("address");
           String phone_number = rs.getString("phone_number");
           String temp1= rs.getInt("temp")+"";
           String patient_job = rs.getString("patient_job");
           String money_paid = rs.getFloat("money_paid")+"";
           String money_remaining = rs.getFloat("money_remaining")+"";
           
           pname.setText(name);
           pAge.setText(age);
           addrress.setText(address);
           phone.setText(phone_number);
           temp.setText(temp1);
           pcashed.setText(money_paid);
           remain.setText(money_remaining);
           pjob.setText(patient_job);
           
          }
          
         
        }catch(SQLException ex )
        {
           
          Logger.getLogger(DBConnection.class.getName()).log(Level.SEVERE, null, ex);
        
        }
        
        
    }//GEN-LAST:event_jButton10ActionPerformed

